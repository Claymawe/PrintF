#include "libft.h"

/*
** Iterates the list 'lst' and applies the function 'f' to the content of
** each element.
*/

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	t_list	*next;

	if (lst != NULL)
	{
		next = lst;
		while (1)
		{
			(*f)(next-> content);
			next = next-> next;
			if (next == NULL)
				return ;
		}
	}
}
